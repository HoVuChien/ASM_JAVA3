        // TODO add your handling code here:
        int row = tblSP.getSelectedRow();
        int i = tblHD.getSelectedRow();
        Integer idSp = (Integer) tblSP.getValueAt(row, 0);
        Integer idhd =  (Integer) tblHD.getValueAt(i, 0); 
        Integer gia = (Integer) tblSP.getValueAt(row, 4);  
        String ten = (String) tblSP.getValueAt(row, 2);
        Integer sl = Integer.parseInt(JOptionPane.showInputDialog(this,"Moi Nhap So Luong"));
        HDCT hdct = new HDCT();
        hdct.setTensp(ten);
        hdct.setGia(gia);
        hdct.setIdhd(idhd);
        hdct.setSl(sl);
        hdct.setNgaytao(new Date());
        hdct.setIdsp(idSp);
        String add = ql.add(hdct);
        loadHDCT(ql.gethdct());
        
