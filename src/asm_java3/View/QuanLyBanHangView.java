 void loadDtSP(ArrayList<SanPham> list){
  defaultTableModel = (DefaultTableModel) tblSP.getModel();
  defaultTableModel.setRowCount(0);
      for (SanPham dm : list) {
          defaultTableModel.addRow(new Object[]{
          dm.getId(),dm.getMaSP(),dm.getTenSP(),dm.getSoLuong(),dm.getGia(),dm.getMauSac()
          });
      }     
 }
 void loaddata(ArrayList<HoaDon> list){
    defaultTableModel= (DefaultTableModel) tblHD.getModel();
    defaultTableModel.setRowCount(0);
    for (HoaDon hd : list) {
        defaultTableModel.addRow(new Object[]{
        hd.getId(),hd.getMaHD(),hd.getNgayTao(),hd.getTen(),hd.getTongTien(),hd.getTrangThai()
        });
        
    }
}
 void loadHDCT(ArrayList<HDCT> list){
    defaultTableModel= (DefaultTableModel) tblHDCT.getModel();
    defaultTableModel.setRowCount(0);
    for (HDCT hd : list) {
        defaultTableModel.addRow(new Object[]{
      hd.getId(),hd.getIdhd(),hd.getIdsp(),hd.getTensp(),hd.getSl(),hd.getGia()
        });
        
    }
}
